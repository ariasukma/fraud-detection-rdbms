CREATE TABLE transactions (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  transaction_id VARCHAR2(64) NOT NULL UNIQUE,
  customer_id VARCHAR2(64) NOT NULL,
  customer_name VARCHAR2(255),
  email VARCHAR2(255),
  phone_number VARCHAR2(50),
  amount NUMBER(18, 2) NOT NULL,
  currency VARCHAR2(10),
  transaction_type VARCHAR2(50),
  transaction_channel VARCHAR2(50),
  transaction_time TIMESTAMP NOT NULL,
  hour_of_day NUMBER(3),
  day_of_week NUMBER(2),
  account_age_days NUMBER(10),
  kyc_status VARCHAR2(30),
  customer_risk_level VARCHAR2(20),
  merchant_id VARCHAR2(64),
  merchant_category VARCHAR2(100),
  merchant_country VARCHAR2(80),
  merchant_risk_level VARCHAR2(20),
  device_id VARCHAR2(64),
  device_type VARCHAR2(50),
  device_os VARCHAR2(50),
  is_new_device NUMBER(1),
  device_change_count_24h NUMBER(10),
  ip_address VARCHAR2(64),
  ip_country VARCHAR2(80),
  ip_city VARCHAR2(100),
  is_vpn NUMBER(1),
  is_proxy NUMBER(1),
  ip_risk_score NUMBER(5, 2),
  source_country VARCHAR2(80),
  destination_country VARCHAR2(80),
  is_cross_border NUMBER(1),
  avg_transaction_amount_30d NUMBER(18, 2),
  transaction_count_1h NUMBER(10),
  transaction_count_24h NUMBER(10),
  failed_login_count_24h NUMBER(10),
  password_change_recently NUMBER(1),
  beneficiary_is_new NUMBER(1),
  beneficiary_age_days NUMBER(10),
  distance_from_last_location_km NUMBER(12, 2),
  minutes_since_last_transaction NUMBER(10),
  days_since_last_transaction NUMBER(10),
  is_fraud NUMBER(1),
  fraud_type VARCHAR2(80),
  fraud_score NUMBER(6, 4),
  prediction_status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
  predicted_at TIMESTAMP,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE INDEX idx_transactions_prediction_status ON transactions (prediction_status);
CREATE INDEX idx_transactions_created_at ON transactions (created_at);

